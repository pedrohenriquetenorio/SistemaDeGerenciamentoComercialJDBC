    public AlterarQtdeMinimaEstoqueView(java.awt.Frame parent, boolean modal, EstoqueModel estoque, ControleEstoqueView estoqueView, String tela) {
        super(parent, modal);
        try {
            initComponents();
            this.estoque = estoque;
            String quantidadeMinima;
            FuncionarioDAO funcionarioDao = new FuncionarioDAO();
            this.estoqueView = estoqueView;
            NumberFormat df = new DecimalFormat("#0.000");
            this.tela = tela;
            if(tela.equals("qtd")){
                jLabel1.setText("Alterar Quantidade");
            }else{
                jLabel1.setText("Alterar Quantidade Minima");
            }
            
            if (estoque.getProduto().getUnidadeMedida().equals("KG")) {

                quantidadeMinima = df.format(estoque.getQtdMinima()).replace(".", "");
                quantidadeMinima = df.format(estoque.getQtdMinima()).replace(",", "");
                jTextFieldQuantidadeMinima.setDocument(new FormatacaoDinamicaQuantidade());
                jTextFieldQuantidadeMinima.setText(quantidadeMinima);
            

            } else {
                Double quantidadeM = estoque.getQtdMinima()/1000;
                quantidadeMinima = df.format(quantidadeM).replace(".", "");
                quantidadeMinima = df.format(quantidadeM).replace(",", "");
                
                jTextFieldQuantidadeMinima.setDocument(new FormatacaoDinamicaQuantidadeUnid());
                jTextFieldQuantidadeMinima.setText(quantidadeMinima);
            }

            System.out.println(estoque.getProduto().getNome());
            this.funcionario = funcionarioDao.pesquisarDataHoralogin();
        } catch (SQLException ex) {
            Logger.getLogger(AlterarQtdeMinimaEstoqueView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

