    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            if(this.tela.equals("qtdMinima")){
               
            EstoqueDAO estoqueDao = new EstoqueDAO();
            String quantidade;
            quantidade = jTextFieldQuantidadeMinima.getText().replace(".", "");
            quantidade = jTextFieldQuantidadeMinima.getText().replace(",", "");
            
            double quantidadeMinimaDouble = Double.parseDouble(quantidade);
            quantidadeMinimaDouble = quantidadeMinimaDouble / 1000;
            
            estoque.setQtdMinima(quantidadeMinimaDouble);
            estoque.setFuncionario(funcionario);
            
            estoqueDao.alteraQuantidade(estoque);
            estoqueView.iniciaTabela();
            
            }else{
                
            EstoqueDAO estoqueDao = new EstoqueDAO();
            String quantidade;
            quantidade = jTextFieldQuantidadeMinima.getText().replace(".", "");
            quantidade = jTextFieldQuantidadeMinima.getText().replace(",", "");
            
            double quantidadeMinimaDouble = Double.parseDouble(quantidade);
            quantidadeMinimaDouble = quantidadeMinimaDouble / 1000;
            
            estoque.setQtdEstoque(quantidadeMinimaDouble);
            estoque.setFuncionario(funcionario);
            
            estoqueDao.alteraQuantidade(estoque);
            estoqueView.iniciaTabela();
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AlterarQtdeMinimaEstoqueView.class.getName()).log(Level.SEVERE, null, ex);
        } 
        super.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

