    public void preencheCampos() throws ParseException{
         Locale localBrasil = new Locale("pt", "BR");
         jTextFieldValor.setDocument(new FormatacaoDinamicaReset());
         String valorTotalString = NumberFormat.getCurrencyInstance(localBrasil).format(this.contaReceber.getValor());
        jTextFieldValor.setText("00");
        jTextFieldValor.setText(valorTotalString);
        jTextFieldNome.setText(this.contaReceber.getCliente().getNome());
        jTextFielddescricao.setText(this.contaReceber.getDescricao());
        String data = new SimpleDateFormat("dd/MM/yyyy").format(this.contaReceber.getDataCompra());
        Mascara mascara = new Mascara();
        
        String celular = mascara.insereMascara(this.contaReceber.getCliente().getCelular(), "celular");
        String cpf = mascara.insereMascara(this.contaReceber.getCliente().getCpf(), "cpf");
        
        jTextFieldCPF.setText(cpf);
        jTextFieldCelular.setText(celular);
        
        
    }
    
