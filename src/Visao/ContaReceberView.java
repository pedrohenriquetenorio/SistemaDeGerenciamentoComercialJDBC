    public void preencheTabela(){
        
        DefaultTableModel val = (DefaultTableModel) jTable1.getModel();
        val.setNumRows(0);
        Locale localBrasil = new Locale("pt", "BR");
        ContasReceberDAO contaReceberDao = new ContasReceberDAO();
        
        ArrayList<ContaReceberModel>listaContaReceber = new ArrayList<>();
      
         try {
             
            listaContaReceber = contaReceberDao.findAll();
            this.listaContaReceber = listaContaReceber;
            listaContaReceber.forEach((contaReceber) -> {
                
                String somaValor = NumberFormat.getCurrencyInstance(localBrasil).format(contaReceber.getValor());
                String dataCadastro = new SimpleDateFormat("dd/MM/yyyy").format(contaReceber.getDataCompra());
                String dataVencimento = new SimpleDateFormat("dd/MM/yyyy").format(contaReceber.getVencimento());
                
                 val.addRow(new Object[]{contaReceber.getId(), contaReceber.getCliente().getNome(),
                 dataCadastro, contaReceber.getDescricao(), dataVencimento,
                 contaReceber.getSituacao()});
                
              });
            
         } catch (SQLException ex) {
             Logger.getLogger(ContaPagarView.class.getName()).log(Level.SEVERE, null, ex);
         } catch (NaoEncontradoException ex) {
             Logger.getLogger(ContaPagarView.class.getName()).log(Level.SEVERE, null, ex);
         }
       
    }
    
