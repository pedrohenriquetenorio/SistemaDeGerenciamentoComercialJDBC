    private void jTextFieldPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarKeyReleased
        int index = jComboBoxTipoPesquisa.getSelectedIndex();
        ContaReceberModel contaReceber = new ContaReceberModel();
        ContasReceberDAO contaReceberDao = new ContasReceberDAO();
        ArrayList<ContaReceberModel> listaContaReceber = new ArrayList<>();
        
        switch (index) {
            case 0: {
                 try //Codigo
                {
                    String codigo = jTextFieldPesquisar.getText();
                    int codigoss = Integer.parseInt(codigo);
                    contaReceber = contaReceberDao.pesquisarID(codigoss);
                    
                } catch (SQLException ex) {
                    Logger.getLogger(AlterarOrcamento.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NaoEncontradoException ex) {
                    Logger.getLogger(AlterarOrcamento.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;

            case 1: {
                try //Codigo
                {
                    
                  String nome = jTextFieldPesquisar.getText();
                  listaContaReceber = contaReceberDao.pesquisarPorNomeCliente(nome);
                  
                } catch (SQLException ex) {
                    Logger.getLogger(AlterarOrcamento.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NaoEncontradoException ex) {
                    Logger.getLogger(AlterarOrcamento.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                    
            }
            break;

        }
    }//GEN-LAST:event_jTextFieldPesquisarKeyReleased

